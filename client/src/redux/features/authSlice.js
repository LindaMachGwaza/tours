import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import * as api from "../api";

//User login using redux toolkit actions. On success of login user is directed to homepage and 
//notified using toast that login was successful
//if not then send an error
export const login = createAsyncThunk(
  "auth/login",
  async ({ formValue, navigate, toast }, { rejectWithValue }) => {
    try {
      const response = await api.signIn(formValue);
      toast.success("Login Successfully");
      navigate("/");
      return response.data;
    } catch (err) {
      return rejectWithValue(err.response.data);
    }
  }
);
//User registration. On success of login user is directed to homepage and 
//notified using toast that login was successful
